    private void setQuestion(){
        String question = ctr.getRandomQuestion();
        jTextFieldQuestion.setText(question);
        jTextFieldAnswer.setText("");
        jLabelCheckstatus.setText("");
    }
    
    private void checkGuess(){
        if(ctr.checkGuess(jTextFieldQuestion.getText(), jTextFieldAnswer.getText()) == true){
            jLabelCheckstatus.setForeground(new Color(0, 150, 0));
            jLabelCheckstatus.setText("Correct");
        } else{
            jLabelCheckstatus.setForeground(Color.red);
            jLabelCheckstatus.setText("False");
        }
    }
    
    private void lookUp(){
        String answer = ctr.lookup(jTextFieldQuestion.getText());
        jTextFieldAnswer.setText(answer);
    }

